<h2>Inundaciones en Brasil</h2>

<img src="Inundaciones Brazil.jpg">

<p>
Brasil ha experimentado numerosas inundaciones a lo largo de su historia debido a lluvias intensas y al desbordamiento de ríos. Estas situaciones afectan principalmente a zonas urbanas con drenaje deficiente, así como a comunidades ubicadas cerca de ríos y áreas bajas, donde el agua se acumula rápidamente causando daños significativos.
</p>

<p>
Las inundaciones pueden destruir viviendas, carreteras y cultivos, afectando la economía y la calidad de vida de miles de personas. Además, representan un riesgo sanitario, ya que el agua contaminada puede propagar enfermedades. Este tipo de desastre suele requerir evacuaciones masivas y asistencia humanitaria urgente.
</p>

<p>
El cambio climático ha incrementado la frecuencia e intensidad de las lluvias en algunas regiones, lo que agrava el problema de las inundaciones. Las autoridades trabajan en proyectos de infraestructura como sistemas de drenaje y represas para reducir el impacto, aunque aún existen desafíos importantes por resolver.
</p>

<p>
La prevención incluye evitar la construcción en zonas de alto riesgo, mejorar los sistemas de drenaje y educar a la población sobre cómo actuar durante una emergencia. Contar con planes de evacuación y mantenerse informado son acciones clave para reducir los daños y proteger vidas.
</p>

