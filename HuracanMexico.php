<h2>Huracán en México</h2>

<img src="Huracan Mexico.jpg">

<p>
México es un país que se ve afectado constantemente por huracanes debido a su ubicación geográfica entre el Océano Pacífico y el Golfo de México. Estos fenómenos meteorológicos se caracterizan por fuertes vientos, lluvias intensas y tormentas eléctricas, que pueden causar graves daños a las comunidades costeras y zonas vulnerables del país.
</p>

<p>
Los huracanes pueden provocar inundaciones, deslizamientos de tierra y destrucción de viviendas. En muchos casos, las comunidades más afectadas son aquellas con menor infraestructura, lo que incrementa el riesgo para la población. Además, la interrupción de servicios básicos como electricidad, agua potable y comunicaciones puede agravar aún más la situación durante y después del evento.
</p>

<p>
El gobierno mexicano cuenta con sistemas de monitoreo y alerta temprana que permiten informar a la población sobre la llegada de huracanes. Estas alertas ayudan a evacuar zonas de riesgo y a preparar refugios temporales para las personas afectadas. La coordinación entre autoridades y ciudadanos es clave para reducir el impacto de estos desastres.
</p>

<p>
La prevención es fundamental para enfrentar los huracanes. Esto incluye asegurar viviendas, almacenar alimentos y agua, y mantenerse informado a través de fuentes oficiales. La educación y la cultura de prevención permiten que las comunidades respondan de manera más eficiente ante este tipo de emergencias.
</p>

