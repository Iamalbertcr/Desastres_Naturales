<h2>Terremoto en Japón</h2>

<img src="Terremoto Japon.png">

<p>
Japón es uno de los países más afectados por los terremotos debido a su ubicación en el llamado Anillo de Fuego del Pacífico. Este fenómeno geológico concentra una gran cantidad de actividad sísmica en el planeta, lo que provoca constantes movimientos de placas tectónicas. Los terremotos en Japón suelen ser de gran intensidad, causando daños significativos en infraestructuras, viviendas y sistemas de transporte, lo que obliga a la población a mantenerse siempre preparada ante posibles emergencias.
</p>

<p>
Uno de los eventos más devastadores ocurrió en 2011, cuando un terremoto de magnitud 9.0 provocó un tsunami que arrasó con ciudades enteras. Este desastre dejó miles de víctimas y generó una crisis nuclear en Fukushima. La combinación de terremoto y tsunami demostró la importancia de contar con sistemas de alerta temprana y protocolos de evacuación efectivos, ya que estos pueden salvar miles de vidas en situaciones críticas.
</p>

<p>
El gobierno japonés ha invertido significativamente en tecnología para reducir el impacto de los terremotos. Entre estas medidas se encuentran edificios antisísmicos, simulacros constantes y sistemas de alerta que notifican a la población segundos antes de que ocurra un movimiento fuerte. Estas acciones han permitido que Japón sea considerado uno de los países mejor preparados ante este tipo de desastres naturales.
</p>

<p>
La educación también juega un papel fundamental en la prevención. Desde edades tempranas, los ciudadanos aprenden cómo reaccionar durante un terremoto, lo que reduce el pánico y mejora la respuesta ante emergencias. Además, las familias suelen contar con kits de emergencia que incluyen agua, alimentos y herramientas básicas, lo que aumenta las probabilidades de supervivencia en caso de desastre.
</p>

