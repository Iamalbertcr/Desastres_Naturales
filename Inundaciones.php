<h2>Inundaciones</h2>

<img src="Inundaciones.jpg">

<p>
Las inundaciones son desastres naturales que ocurren cuando grandes cantidades de agua cubren áreas que normalmente están secas. Estas pueden ser causadas por lluvias intensas, desbordamiento de ríos, huracanes o fallas en sistemas de drenaje. Las inundaciones afectan tanto zonas rurales como urbanas, generando pérdidas materiales significativas y poniendo en riesgo la vida de las personas que habitan en áreas vulnerables.
</p>

<p>
Uno de los principales problemas asociados a las inundaciones es la destrucción de viviendas, carreteras y cultivos. Además, el agua contaminada puede provocar enfermedades y afectar la salud pública. En muchos casos, las personas deben ser evacuadas y trasladadas a refugios temporales mientras se restablecen las condiciones normales en las zonas afectadas.
</p>

<p>
El cambio climático ha incrementado la frecuencia e intensidad de las lluvias en diversas regiones del mundo, lo que aumenta el riesgo de inundaciones. Las autoridades trabajan en la construcción de sistemas de drenaje, represas y otras infraestructuras para mitigar estos efectos, aunque la prevención sigue siendo un reto importante en muchas ciudades.
</p>

<p>
La prevención es clave para reducir el impacto de las inundaciones. Evitar la construcción en zonas propensas, mantener limpios los sistemas de drenaje y contar con planes de evacuación son medidas fundamentales. La educación y la concienciación de la población permiten una mejor respuesta ante emergencias y ayudan a salvar vidas.
</p>

